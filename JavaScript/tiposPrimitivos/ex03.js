console.log("a || 'padrao' =", a || "padrao");
console.log("b || 'padrao' =", b || "padrao");
console.log("c || 'padrao' =", c || "padrao");
console.log("d || 'padrao' =", d || "padrao");

console.log("\nUsando ?? (Nullish Coalescing):");
console.log("a ?? 'padrao' =", a ?? "padrao");
console.log("b ?? 'padrao' =", b ?? "padrao");
console.log("c ?? 'padrao' =", c ?? "padrao");
console.log("d ?? 'padrao' =", d ?? "padrao");
